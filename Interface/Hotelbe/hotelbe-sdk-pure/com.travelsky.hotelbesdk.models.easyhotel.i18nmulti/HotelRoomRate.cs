namespace com.travelsky.hotelbesdk.models.easyhotel.i18nmulti
{
    using System;

    public class HotelRoomRate
    {
        private string date;
        private string price;

        public string Date
        {
            get
            {
                return this.date;
            }
            set
            {
                this.date = value;
            }
        }

        public string Price
        {
            get
            {
                return this.price;
            }
            set
            {
                this.price = value;
            }
        }
    }
}

